package riven.core.entity.identity

import io.hypersistence.utils.hibernate.type.json.JsonBinaryType
import jakarta.persistence.Column
import jakarta.persistence.Entity
import jakarta.persistence.EnumType
import jakarta.persistence.Enumerated
import jakarta.persistence.GeneratedValue
import jakarta.persistence.GenerationType
import jakarta.persistence.Id
import jakarta.persistence.Table
import org.hibernate.annotations.Type
import riven.core.entity.util.AuditableSoftDeletableEntity
import riven.core.enums.identity.MatchSuggestionStatus
import riven.core.models.common.json.JsonObject
import riven.core.models.identity.MatchSuggestion
import java.math.BigDecimal
import java.time.ZonedDateTime
import java.util.UUID

/**
 * JPA entity for match_suggestions table.
 *
 * Represents a candidate entity pair generated by the identity resolution engine,
 * awaiting human review. Source/target UUIDs are kept in canonical order
 * (source_entity_id < target_entity_id) enforced by a DB CHECK constraint.
 */
@Entity
@Table(name = "match_suggestions")
data class MatchSuggestionEntity(
    @Id
    @GeneratedValue(strategy = GenerationType.UUID)
    @Column(name = "id", nullable = false, columnDefinition = "uuid")
    val id: UUID? = null,

    @Column(name = "workspace_id", nullable = false, columnDefinition = "uuid")
    val workspaceId: UUID,

    @Column(name = "source_entity_id", nullable = false, columnDefinition = "uuid")
    val sourceEntityId: UUID,

    @Column(name = "target_entity_id", nullable = false, columnDefinition = "uuid")
    val targetEntityId: UUID,

    @Enumerated(EnumType.STRING)
    @Column(name = "status", nullable = false, length = 20)
    var status: MatchSuggestionStatus = MatchSuggestionStatus.PENDING,

    @Column(name = "confidence_score", nullable = false, precision = 5, scale = 4)
    val confidenceScore: BigDecimal,

    @Type(JsonBinaryType::class)
    @Column(name = "signals", nullable = false, columnDefinition = "jsonb")
    var signals: JsonObject = emptyMap(),

    @Type(JsonBinaryType::class)
    @Column(name = "rejection_signals", columnDefinition = "jsonb")
    var rejectionSignals: JsonObject? = null,

    @Column(name = "resolved_by", columnDefinition = "uuid")
    var resolvedBy: UUID? = null,

    @Column(name = "resolved_at", columnDefinition = "timestamptz")
    var resolvedAt: ZonedDateTime? = null,
) : AuditableSoftDeletableEntity() {

    /** Convert this entity to the domain model. */
    fun toModel(): MatchSuggestion {
        val id = requireNotNull(this.id) { "MatchSuggestionEntity ID must not be null" }
        return MatchSuggestion(
            id = id,
            workspaceId = workspaceId,
            sourceEntityId = sourceEntityId,
            targetEntityId = targetEntityId,
            status = status,
            confidenceScore = confidenceScore,
            signals = signals,
            rejectionSignals = rejectionSignals,
            resolvedBy = resolvedBy,
            resolvedAt = resolvedAt,
            createdAt = requireNotNull(createdAt) { "MatchSuggestionEntity createdAt must not be null" },
            updatedAt = requireNotNull(updatedAt) { "MatchSuggestionEntity updatedAt must not be null" },
        )
    }
}
